def seed_foods_if_empty(db: Session) -> None:
    existing_foods = db.query(Food).all()
    existing_keys = set()
    for row in existing_foods:
        key = (
            (row.name or "").strip().lower(),
            (row.brand or "").strip().lower(),
            (row.barcode or "").strip(),
        )
        existing_keys.add(key)

    for food_data in SEED_FOODS:
        key = (
            (food_data.get("name") or "").strip().lower(),
            (food_data.get("brand") or "").strip().lower(),
            (food_data.get("barcode") or "").strip(),
        )
        if key in existing_keys:
            continue
        db.add(Food(**food_data))

    db.commit()
